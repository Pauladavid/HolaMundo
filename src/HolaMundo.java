public class HolaMundo {
    public static void main(String[] args) {
        String saludar="Hola, como estan. Mi nombre es Paula David";
        System.out.println(saludar + " Primer proyecto");
        System.out.println(saludar.toUpperCase());
        int numero= 10;
        System.out.println(numero + " es un numero entero");
        float numeroDecimal= 12.02f;
        System.out.println(numeroDecimal + " es un numero flotante");
        double numeroDouble= 12.25;
        System.out.println(numeroDouble + " es un numero Double");
        boolean valorT= true;
        boolean valorF= false;
        System.out.println(valorT+" y "+valorF+ " son valores Booleanos:");

    }
}
